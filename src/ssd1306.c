void ssd1306_draw_char(int x, int page, char c) {
  if (c < 0x20 || c > 0x7E)
    return;
  const uint8_t *glyph = font5x8[c - 0x20];
  for (int col = 0; col < 5; col++) {
    if (x + col < 128)
      fb[page][x + col] = glyph[col];
  }
}

void ssd1306_draw_string(int x, int page, const char *str) {
  while (*str) {
    ssd1306_draw_char(x, page, *str++);
    x += 6; /* 5px glyph + 1px gap */
    if (x > 122)
      break;
  }
}

void ssd1306_fill(uint8_t val) {
  for (int p = 0; p < 8; p++)
    for (int x = 0; x < 128; x++)
      fb[p][x] = val;
}
