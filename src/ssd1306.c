#include "ssd1306.h"
#include "i2c.h" // your i2c1_burst_write / i2c1_byte_read header

/* 128x64 framebuffer: 8 pages of 128 bytes each (1 bit per pixel) */
static uint8_t fb[SSD1306_HEIGHT / 8][SSD1306_WIDTH];

/* ---- low-level helpers ------------------------------------------ */

static void ssd1306_cmd(uint8_t cmd) {
  char buf[1] = {(char)cmd};
  i2c1_burst_write(SSD1306_ADDR, 0x00, 1, buf);
}
static void ssd1306_data(uint8_t *data, int len) {
  i2c1_burst_write(SSD1306_ADDR, 0x40, len - 1, (char *)data + 1);
}

/* ---- init sequence ---------------------------------------------- */

void ssd1306_init(void) {
  /* Standard SSD1306 128x64 init sequence */
  ssd1306_cmd(0xAE); /* display off */
  ssd1306_cmd(0xD5);
  ssd1306_cmd(0x80); /* clock div */
  ssd1306_cmd(0xA8);
  ssd1306_cmd(63); /* mux ratio (64 rows - 1) */
  ssd1306_cmd(0xD3);
  ssd1306_cmd(0x00); /* display offset */
  ssd1306_cmd(0x40); /* start line = 0 */
  ssd1306_cmd(0x8D);
  ssd1306_cmd(0x14); /* charge pump ON */
  ssd1306_cmd(0x20);
  ssd1306_cmd(0x00); /* horizontal addressing mode */
  ssd1306_cmd(0xA1); /* segment remap */
  ssd1306_cmd(0xC8); /* COM scan direction remapped */
  ssd1306_cmd(0xDA);
  ssd1306_cmd(0x12); /* COM pins config */
  ssd1306_cmd(0x81);
  ssd1306_cmd(0xCF); /* contrast */
  ssd1306_cmd(0xD9);
  ssd1306_cmd(0xF1); /* precharge */
  ssd1306_cmd(0xDB);
  ssd1306_cmd(0x40); /* VCOMH deselect */
  ssd1306_cmd(0xA4); /* display from RAM */
  ssd1306_cmd(0xA6); /* normal (not inverted) */
  ssd1306_cmd(0xAF); /* display ON */
}

/* ---- framebuffer ops -------------------------------------------- */

void ssd1306_clear(void) {
  for (int p = 0; p < 8; p++)
    for (int x = 0; x < 128; x++)
      fb[p][x] = 0x00;
}

void ssd1306_draw_pixel(int x, int y, uint8_t on) {
  if (x < 0 || x >= 128 || y < 0 || y >= 64)
    return;
  int page = y / 8;
  int bit = y % 8;
  if (on)
    fb[page][x] |= (1 << bit);
  else
    fb[page][x] &= ~(1 << bit);
}

/* ---- flush to display ------------------------------------------- */

void ssd1306_update(void) {
  for (int p = 0; p < 8; p++) {
    ssd1306_cmd(0xB0 + p); /* page address */
    ssd1306_cmd(0x10);     /* lower column = 0 */
    ssd1306_cmd(0x10);     /* upper column = 0 */
    ssd1306_data(fb[p], 128);
  }
}

